### Operadores ###
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(3 % 4)

print(3 // 4) # Floor division
print(3 ** 4) # Exponente

# Con strings
print("Hola" + " Mundo")
print("Hola " * 5)
print ("Hola " + str(5)) # No se podría utilizar el 5 como entero, da error

# Operadores comparativos
print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

# Sirven para cadenas de texto
print("Hola" != "Papa")
print("Hola" == "Hola")
print("Hola" > "Python") # Compara las ordenaciones alfabéticas de dos cadenas
print("AAAAA" >= "aaaaa")

# Operadores lógicos
print(3 < 4 and 4 < 5)
print(True or False)
print(not False and 3 < 50)